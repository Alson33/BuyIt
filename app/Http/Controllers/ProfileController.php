    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Profile  $profile
     * @return \Illuminate\Http\Response
     */
    public function edit(Profile $profile)
    {
        $userprofile = Profile::findOrFail($profile->id);

        $info['profile'] = $userprofile;

        return view('profile.edit', $info);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Profile  $profile
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Profile $profile)
    {
        $request->validate([
            'address' => 'required',
            'phone_number' => 'required',
        ]);

        $data = $request->all();

        $userprofile = Profile::findOrFail($profile->id);
        $userprofile->update($data);

        if($data['image']){
            if($userprofile->getFirstMediaUrl('profile_images'))
                $userprofile->clearMediaCollection('profile_images');

            $userprofile->addMediaFromRequest('image')->toMediaCollection('profile_images');
        }

        return redirect()->route('home')->with('success', 'Profile updated successfully');
    }

